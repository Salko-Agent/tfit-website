<p>Ein weiterer wichtiger Aspekt des Krafttrainings ist, dass es nicht nur Fett abbaut, sondern auch die Körperkomposition verändert. Da Muskeln dichter sind als Fett, kann es passieren, dass sich das Gewicht auf der Waage nicht stark ändert, der Körper jedoch sichtbar schlanker und fitter wirkt.</p>

